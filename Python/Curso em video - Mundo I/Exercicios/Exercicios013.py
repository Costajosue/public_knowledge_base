# FAÇA UM PROGRAMA QUE LEIA O SÁLARIO E MOSTRE SEU NOVO, COM 15% DE AUMENTO.


salario = float(input('Qual o seu salario esse mês R$: '))
novo = salario + (salario * 15 / 100)
print(' seu novo salario passara de R$: {:.2f}, para R$: {:.2f} '.format(salario, novo))

